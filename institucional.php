      <p style="text-align: justify;">
        La Caja de Jubilaciones Municipal se crea el 31 de mayo de 1907 para los empleados al servicio de la administración municipal otorgando sus primeros beneficios en 1912. La Ordenanza que le dio vida fue modificada a través del tiempo
  			 en lo que hace al régimen de sus prestaciones que permitió la incorporación del personal del Banco Municipal, otros entes autárquicos municipales y diversas comunas de la provincia de Santa Fe.
  			En julio de 1977, la Caja se trasnformó en el Insituto de Previsión  Social  de  Rosario. En octubre de 1981 absorbe dos entidades municipales llamas "Seguro  y Subsidio Mutual Municipal" y "Fondo Especial Ordenanza 436/48".
  			En el año 1995 al generarse el proceso de absorción de entidades previsionales provinciales y municipales por parte de la Nación y el advenimiento de las AFJP se introdujeron reformas sustanciales en la legislación vigente a través de la sanción de la Ordenanza 611/95 con el objetivo de consevar a la institución y todos sus afiliados activos y pasivos en la órbita de la administración pública municipal.
  			En el año 2005 mediante la Ordenanza Nº 7919/05 se volvieron a introducir nuevas reformas legislativas, tendientes en general a mejorar las condiciones y requisitos exigidos a los afiliados para el otorgamiento de las prestaciones previsionales.
  			En lo que concierne al espacio en que funcionó la institución, la Caja Municipal de Jubilaciones y Pensiones de Rosario inició sus actividades en las intalaciones del viejo edificio del Banco Municipal de Rosario, y continuó en ellas hasta el año 1936 en que se trasladó a la calle Córdoba 954. Finalmente en el año 1958 adquirió el histórico edificio cuya actual construcción data de 1908, sito en la calle San Lorenzo Nº 1055, donde continuó presentando sus servicios desde el año 1959 hasta el presente.
  			En el año 2001 se decide realizar una amplia remodelación, ampliación y modernización del mencionado edificio, con el objeto de dotar al organismo con más amplias y mejores intalaciones y equipamiento para la prestación de los servicios a los afiliados.No obstante el alcance de las reformas interiores, se preserva la fachada por tratarse de un edificio que por su antigüedad y riqueza estilistica ha sido declarado Patrimonio Histórico Arquitectónico de Rosario.
